CREATE PROCEDURE NMP1051
( 
	 @DivisionID VARCHAR(50),
	 @UserID VARCHAR(50),
	 @XML XML
)


AS 
DECLARE @sSQL NVARCHAR (MAX) = N'',
		@LanguageID VARCHAR(50)

SELECT TOP 1 @LanguageID = ISNULL(LanguageID,'') FROM AT14051 WITH (NOLOCK) WHERE UserID = @UserID

CREATE TABLE #NMP1051 (APK VARCHAR(50))
INSERT INTO #NMP1051 (APK)
SELECT X.Data.query('APK').value('.', 'NVARCHAR(50)') AS APK
FROM @XML.nodes('//Data') AS X (Data)	

SET @sSQL = @sSQL + N'
SELECT NMT1040.DivisionID, NMT1040.DishTypeID, NMT1040.DishTypeName,NMT1040.Description,
FROM NMT1040 WITH (NOLOCK)
INNER JOIN #NMP1051 ON NMT1040.APK = #NMP1051.APK
ORDER BY NMT1040.DishTypeID'

--PRINT(@sSQL)
EXEC (@sSQL)


   
GO
SET QUOTED_IDENTIFIER OFF
GO
SET ANSI_NULLS ON
GO
